import "@/styles/globals.css";

import { notFound } from "next/navigation";
import { NextIntlClientProvider, useMessages } from "next-intl";

import { Footer } from "@/components/footer/footer";
import { Header } from "@/components/header/header";
import { LanguageLinksProvider } from "@/lib/contexts/language-links-context";

import { i18nConfig } from "@/i18n";

export const metadata = {
  title: "Next.js",
  description: "Generated by Next.js",
};

interface LayoutProps {
  params: {
    locale: string;
  };
  children: React.ReactNode;
}

export default function Layout({ children, params: { locale } }: LayoutProps) {
  const messages = useMessages();

  // Validate that the incoming `locale` parameter is valid
  if (!i18nConfig.locales.includes(locale)) notFound();

  return (
    <html lang={locale}>
      <body>
        <NextIntlClientProvider locale={locale} messages={messages}>
          <LanguageLinksProvider>
            <div className="flex min-h-screen flex-col">
              <Header />
              <main className="grow bg-mischka" id="main-content">
                <div className="mx-auto max-w-6xl px-6 py-8">{children}</div>
              </main>
              <Footer locale={locale} />
            </div>
          </LanguageLinksProvider>
        </NextIntlClientProvider>
      </body>
    </html>
  );
}
